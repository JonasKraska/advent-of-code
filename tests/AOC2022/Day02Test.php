enum Outcome {
    case LOSE;
    case DRAW;
    case WIN;

    public static function fromExpectedOutcome(string $expectedOutcome): self
    {
        return match(strtoupper($expectedOutcome)) {
            'X' => self::LOSE,
            'Y' => self::DRAW,
            'Z' => self::WIN,
            default => throw new \LogicException(),
        };
    }

    public static function fromMoves(Move $opponent, Move $own): self
    {
        return match (true) {
            // DRAW
            $opponent === $own => self::DRAW,

            // WIN
            $opponent === Move::ROCK && $own === Move::PAPER,
            $opponent === Move::PAPER && $own === Move::SCISSOR,
            $opponent === Move::SCISSOR && $own === Move::ROCK => self::WIN,

            // LOSE
            default => self::LOSE,
        };
    }
}

enum Move {
    case ROCK;
    case PAPER;
    CASE SCISSOR;

    public static function fromOpponent(string $opponent): self
    {
        return match(strtoupper($opponent)) {
            'A' => self::ROCK,
            'B' => self::PAPER,
            'C' => self::SCISSOR,
            default => throw new \LogicException(),
        };
    }

    public static function fromOwn(string $own): self
    {
        return match(strtoupper($own)) {
            'X' => self::ROCK,
            'Y' => self::PAPER,
            'Z' => self::SCISSOR,
            default => throw new \LogicException(),
        };
    }
