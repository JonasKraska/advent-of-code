    /**
     * @dataProvider provideInput
     */
    public function testPart1(string $input): void
    {
        $rounds = explode(PHP_EOL, $input);

        $score = 0;
        foreach ($rounds as $round) {
            [$opponentMove, $ownMove] = explode(' ', $round);

            $opponent = Move::fromOpponent(trim($opponentMove));
            $own = Move::fromOwn(trim($ownMove));

            $outcome = Outcome::fromMoves($opponent, $own);

            $score += $this->calcRound($own, $outcome);
        }

        self::assertSame(13446, $score);
    }

    /**
     * @dataProvider provideInput
     */
    public function testPart2(string $input): void
    {
        $rounds = explode(PHP_EOL, $input);

        $score = 0;
        foreach ($rounds as $round) {
            [$opponent, $outcome] = explode(' ', $round);

            $opponentMove = Move::fromOpponent(trim($opponent));
            $expectedOutcome = Outcome::fromExpectedOutcome(trim($outcome));

            $ownMove = $this->calcMove($opponentMove, $expectedOutcome);

            $score += $this->calcRound($ownMove, $expectedOutcome);
        }

        self::assertSame(13509, $score);
    }

    private function calcRound(Move $own, Outcome $outcome): int
    {
        $moveScore = match ($own) {
            Move::ROCK => 1,
            Move::PAPER => 2,
            Move::SCISSOR => 3,
        };

        $outcomeScore = match ($outcome) {
            Outcome::DRAW => 3,
            Outcome::WIN => 6,
            Outcome::LOSE => 0,
        };

        return $moveScore + $outcomeScore;
    }

    private function calcMove(Move $opponent, Outcome $expectedOutcome): Move
    {
        foreach (Move::cases() as $move) {
            $outcome = Outcome::fromMoves($opponent, $move);

            if ($outcome === $expectedOutcome) {
                return $move;
            }
        }
    }

